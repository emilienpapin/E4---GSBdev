            <h3>Le secteur d'activité</h3>

<p>L’industrie pharmaceutique est un secteur très lucratif dans lequel le mouvement de fusion acquisition est très fort.

Les regroupements de laboratoires ces dernières années ont donné naissance à des entités gigantesques au sein

desquelles le travail est longtemps resté organisé selon les anciennes structures. 

Des déboires divers récents autour de médicaments ou molécules ayant entraîné des complications médicales ont

fait s'élever des voix contre une partie de l'activité des laboratoires : la visite médicale, réputée être le lieu d'

arrangements entre l'industrie et les praticiens, et tout du moins un terrain d'influence opaque.</p>

<h3>L'entreprise</h3>

<p>Le laboratoire Galaxy Swiss Bourdin (GSB) est issu de la fusion entre le géant américain Galaxy (spécialisé dans

le secteur des maladies virales dont le SIDA et les hépatites) et le conglomérat européen Swiss Bourdin (travaillant

sur des médicaments plus conventionnels), lui même déjà union de trois petits laboratoires .

En 2009, les deux géants pharmaceutiques ont uni leurs forces pour créer un leader de ce secteur industriel.

L'entité Galaxy Swiss Bourdin Europe a établi son siège administratif à Paris. 

Le siège social de la multinationale est situé à Philadelphie, Pennsylvanie, aux Etats-Unis.

La France a été choisie comme témoin pour l'amélioration du suivi de l'activité de visite.</p>
